    private void table1() {//Menampilkan table ksp dari database di table1
        DefaultTableModel data = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        table1.getRowCount();
        table1.setModel(data);
        data.addColumn("ID");
        data.addColumn("Tanggal");
        data.addColumn("Pinjaman Anggota");
        data.addColumn("Simpanan Anggota");

        TableColumn column;
        table1.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
        column = table1.getColumnModel().getColumn(0);
        column.setPreferredWidth(54);
        column = table1.getColumnModel().getColumn(1);
        column.setPreferredWidth(80);
        column = table1.getColumnModel().getColumn(2);
        column.setPreferredWidth(192);
        column = table1.getColumnModel().getColumn(3);
        column.setPreferredWidth(191);

        String sql = "SELECT * FROM ksp"; //Menampilkan semua data ksp di table1
        try {
            st = koneksi.con.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                String kolom1 = rs.getString(2); //Mendapatkan semua data
                String kolom2 = rs.getString(8);
                String kolom3 = rs.getString(13);
                String kolom4 = rs.getString(11);
                String kolom[] = {kolom1, kolom2, kolom3, kolom4};
                data.addRow(kolom);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Tidak terhubung dengan database\n" + e,
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

