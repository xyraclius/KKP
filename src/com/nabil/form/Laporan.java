    private void tsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tsimpanActionPerformed
        utilities.clickGo();
        int ok = JOptionPane.showConfirmDialog(null, "Apa kamu yakin ingin menyimpan data ini?",
                "CONFIRMATION", JOptionPane.YES_NO_OPTION);
        try { //Tombol Simpan
            if (ok == JOptionPane.YES_OPTION) {
                utilities.clickGo();
                st = koneksi.con.createStatement();
                String sql = "INSERT INTO kspt(Tgl_Laporan,"
                        + "Jml_Pnj_SW,"
                        + "Jml_Smp_SW,"
                        + "Jml_Pnj_BI,"
                        + "Jml_Smp_Bi)"
                        + "VALUES ('" + currentdate
                        + "','" + fpinjam_semua_waktu.getText()
                        + "','" + FTSimpanan_Semua_Waktu.getText()
                        + "','" + FTPinjaman_Bulan_Ini.getText()
                        + "','" + FTSimpan_Bulan_Ini.getText() + "')";
                String sql2 = "ALTER TABLE kspt DROP No";
                String sql3 = "ALTER TABLE kspt ADD  No INT( 11 ) NOT NULL AUTO_INCREMENT FIRST ,ADD PRIMARY KEY (No)";
                st.executeUpdate(sql);
                st.executeUpdate(sql2);
                st.executeUpdate(sql3);
                table3();
                utilities.clickSuccess();
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan",
                        "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                tbersihActionPerformed(evt);
            } else if (ok == JOptionPane.NO_OPTION) {
                utilities.clickCancel();
                tbersihActionPerformed(evt);
            }
        } catch (SQLException e) {
            utilities.clickError();
            JOptionPane.showMessageDialog(null, "Data tidak berhasil tersimpan\n" + e);
            tbersihActionPerformed(evt);
        }
    }//GEN-LAST:event_tsimpanActionPerformed

    private void thapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_thapusActionPerformed
        utilities.clickGo();
        int ok = JOptionPane.showConfirmDialog(null, "Apa kamu yakin ingin menghapus data ini?",
                "CONFIRMATION", JOptionPane.YES_NO_OPTION);
        try { //Delete Record
            if (ok == JOptionPane.YES_OPTION) {
                utilities.clickGo();
                String sql = "delete from kspt where No=?";
                pst = koneksi.con.prepareStatement(sql);
                pst.setString(1, nomor.getText());
                pst.execute();
                String sql2 = "ALTER TABLE kspt DROP No";
                String sql3 = "ALTER TABLE kspt ADD  No INT( 11 ) NOT NULL AUTO_INCREMENT FIRST ,ADD PRIMARY KEY (No)";
                st.executeUpdate(sql2);
                st.executeUpdate(sql3);
                table3();
                tbersihActionPerformed(evt);
                utilities.clickSuccess();
                JOptionPane.showMessageDialog(null, "Data berhasil terhapus");
            } else if (ok == JOptionPane.NO_OPTION) {
                utilities.clickCancel();
                tbersihActionPerformed(evt);
            }
        } catch (SQLException e) {
            utilities.clickFailure();
            JOptionPane.showMessageDialog(null, "Data tidak terhapus\n" + e,
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_thapusActionPerformed

    private void table3MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table3MouseReleased
        utilities.clickGo();
        int a = table3.getSelectedRow();
        if (a >= 0) {
            table3.setRowSelectionInterval(a, a);
            thapus.setEnabled(true);
            int baris = table3.getSelectedRow();
            TableModel model = table3.getModel();
            nomor.setText(model.getValueAt(baris, 0).toString());
            FTSimpanan_Semua_Waktu.setText(model.getValueAt(baris, 2).toString());
            fpinjam_semua_waktu.setText(model.getValueAt(baris, 3).toString());
            FTSimpan_Bulan_Ini.setText(model.getValueAt(baris, 4).toString());
            FTPinjaman_Bulan_Ini.setText(model.getValueAt(baris, 5).toString());
        } else {
            thapus.setEnabled(false);
            FTPinjaman_Bulan_Ini.setText("");
            FTSimpan_Bulan_Ini.setText("");
        }
    }//GEN-LAST:event_table3MouseReleased

